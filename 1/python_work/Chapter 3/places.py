#Store at least 5 location in a list. 
places = ['Alba-Iulia', 'Petrosani', 'Varful Moldoveanu', 'Switserland', 'Turkey']

#Print list in original order
print(places)

#Use sorted() to print them in alphabetical order
print(sorted(places))

#Show list is in it's original order still
print(places)

#Use reverse and print the list 
places.reverse()
print(places)

#Use reverse again
places.reverse()
print(places)

#Use sort() to store the list in alphabetical order
places.sort()
print(places)

#Use sort in reverse
places.sort(reverse=True)
print(places)